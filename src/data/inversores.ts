// ─── Helpers de busca ─────────────────────────────────────────────────────────

/** Busca inversor por ID */
export function buscarInversorPorId(id: string): InversorFotovoltaico | undefined {
  return inversores.find((inv) => inv.id === id);
}

/** Filtra inversores por fabricante */
export function filtrarInversoresPorFabricante(fabricante: string): InversorFotovoltaico[] {
  return inversores.filter((inv) => inv.fabricante.toLowerCase() === fabricante.toLowerCase());
}

/** Filtra inversores por fase (monofásico ou trifásico) */
export function filtrarInversoresPorFase(fase: 'monofasico' | 'trifasico'): InversorFotovoltaico[] {
  return inversores.filter((inv) => inv.fase === fase);
}

/** Filtra inversores por faixa de potência nominal em kW */
export function filtrarInversoresPorPotencia(
  minKw: number,
  maxKw: number
): InversorFotovoltaico[] {
  return inversores.filter(
    (inv) => inv.potenciaNominal >= minKw && inv.potenciaNominal <= maxKw
  );
}
